// Differences btwn * and &:

// Asterisks (*)

// The asterisks (*) is used before a variable name to indicate that the variable is a pointer (pointer to a value)
// Also used for dereferencing a pointer (access or change the value it is set to)

// Ampersand (&)

// The ampersand is used to indicate a reference data type => int(&b = a)
// Can also be used to take the address of a variable => (int *pointer = &x)



